#include <math.h>
#include "calc.h"

double add(double x1, double x2) { return x1 + x2; }
double sub(double x1, double x2) { return x1 - x2; }
double mul(double x1, double x2) { return x1 * x2; }
double div(double x1, double x2) { return x1 / x2; }
double exp(double x1, double x2) { return pow(x1, x2); }